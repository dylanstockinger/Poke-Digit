

function toggleColor() {

    if (document.getElementById("btn").style.backgroundColor = "#f07b3f") {
        document.getElementById("btn").style.backgroundColor = "red";
        document.getElementById("btn").innerHTML = "IM RED !"

    } else {
        document.getElementById("btn").style.backgroundColor = "#f07b3f"
        document.getElementById("btn").innerHTML = "IM BACK BABY !"

    }

    }
  
    